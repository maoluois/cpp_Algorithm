#include <bits/stdc++.h>
using namespace std;
int main(){	
	int a = 1 ,b = 23, c = 456;
	
	
	cout<<"123456789012345678901234567890"<<endl;
	
	printf("%5d%5d%5d\n", a, b, c); 
	cout<<"123456789012345678901234567890"<<endl;
	printf("%-5d%-5d%-5d\n", a, b, c); 
	cout<<"123456789012345678901234567890"<<endl;
	printf("%05d%05d%05d\n", a, b, c); 
	
	
	return 0;	
}

